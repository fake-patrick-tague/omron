package com.google.android.gms.internal.measurement;

public final class zzpf
  implements zzih
{
  private static final zzpf y = new zzpf();
  private final zzih value;
  
  public zzpf()
  {
    value = zzil.toLowerCase(localZzih);
  }
  
  public static boolean initDbTable()
  {
    return y.linearCombination().getOrElse();
  }
  
  public static boolean zoomIn()
  {
    y.linearCombination().canZoomIn();
    return true;
  }
  
  public final zzpg linearCombination()
  {
    return (zzpg)value.dotProduct();
  }
}
